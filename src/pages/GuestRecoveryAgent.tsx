import { RecommendationCard } from "@/components/RecommendationCard";
import { AgentTimeline } from "@/components/AgentTimeline";
import { AgentChat } from "@/components/AgentChat";
import { StatusBadge } from "@/components/StatusBadge";
import { guests as mockGuests, incidents as mockIncidents, agentRecommendations as mockRecommendations, guestRecoveryTimeline as mockTimeline } from "@/data/mockData";
import { User, Crown, CreditCard, Ship, MessageSquare, Star } from "lucide-react";
import { useQuery } from "@tanstack/react-query";
import { api } from "@/lib/api";
import { parseTimestamp } from "@/lib/utils";

// ┌─────────────────────────────────────────────────────────────────────────────┐
// │ COUCHBASE INTEGRATION: Guest Recovery Agent Data                           │
// │                                                                             │
// │ Replace static data with live Couchbase queries:                           │
// │                                                                             │
// │ OPTION A — Couchbase Capella:                                              │
// │   • Guest profile: GET from voyageops.guests.profiles via Capella SDK      │
// │   • Incidents: SQL++ query on voyageops.operations.incidents               │
// │   • Recommendations: Generated by Capella AI Services (RAG pipeline)       │
// │     using vector search on guest history embeddings                         │
